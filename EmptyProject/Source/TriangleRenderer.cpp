//-------------------------------------------------------------
//!	@brief		: 三角形描画
//-------------------------------------------------------------
void TriangleRenderer::Draw()
{
	if (!m_IsActive)
		return;

	assert(m_Device);
	assert(m_IndexBuffer);
	m_Device->SetTransform(D3DTS_WORLD, &m_World);
	m_Device->SetIndices(m_IndexBuffer);
	//	TODO:各種引数をちゃんと設定
	m_Device->DrawIndexedPrimitive(
		D3DPT_TRIANGLELIST, 
		0,
		m_IndexData.start,
		m_Index.size(),
		0, 
		m_IndexData.face);
}


