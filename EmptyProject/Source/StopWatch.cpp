#include "StopWatch.h"
using namespace AK;

//=======================================================================================
//		Constants Definitions
//=======================================================================================

//-------------------------------------------------------------
//!	@brief		: コンストラクタ
//-------------------------------------------------------------
StopWatch::StopWatch()
	:	m_Time		(0.f)
	,	m_IsStop	(false)
{
}

//-------------------------------------------------------------
//!	@brief		: デストラクタ
//-------------------------------------------------------------
StopWatch::~StopWatch()
{
}
//=======================================================================================
//		public method
//=======================================================================================
//-------------------------------------------------------------
//!	@brief		: example
//!	@param[in]	: example
//!	@return		: example
//-------------------------------------------------------------
void StopWatch::Start()
{
	m_IsStop = false;
}
//-------------------------------------------------------------
//!	@brief		: example
//!	@param[in]	: example
//!	@return		: example
//-------------------------------------------------------------
void StopWatch::Stop()
{
	m_IsStop = true;
}
//-------------------------------------------------------------
//!	@brief		: example
//!	@param[in]	: example
//!	@return		: example
//-------------------------------------------------------------
void StopWatch::Update(F32 elapsedTime)
{
	if (m_IsStop)
		return;
	m_Time += elapsedTime;
}
//-------------------------------------------------------------
//!	@brief		: example
//!	@param[in]	: example
//!	@return		: example
//-------------------------------------------------------------
void StopWatch::Reset()
{
	m_Time = 0.f;
}
//-------------------------------------------------------------
//!	@brief		: example
//!	@param[in]	: example
//!	@return		: example
//-------------------------------------------------------------
F32	 StopWatch::GetTime() const
{
	return m_Time;
}
